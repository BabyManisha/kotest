define(['ko'], function(ko) {
	'use strict';

	function Component(params) {
		this.username = params.username;
		this.password = params.password;
		this.credentials = ko.computed(function() {
			return {
				username: ko.utils.unwrapObservable(this.username),
				password: ko.utils.unwrapObservable(this.password)
			};
		}, this);
		this.onSubmit = params.onSubmit;

		this.disposed = params.disposed;
	}

	Component.prototype.submit = function(vm) {
		this.onSubmit(this.credentials());
		return false;
	};

	Component.prototype.dispose = function() {
		this.disposed(true);
		this.credentials.dispose();
	};

	return Component;
});